// ✅ このコードは Java の「メソッドオーバーロード（同名メソッドの多重定義）」の例である。
// greet() と greet(String) という同じ名前のメソッドを、引数リスト（シグネチャ）違いで2つ定義している。
// 呼び出し側は引数の個数・型に応じて、コンパイラがどちらを呼ぶかを解決する。
//
// アルゴリズム的には：
// 1) 呼び出し式 greet(...) を見て候補メソッド集合を集める
// 2) 引数の型・個数に一致する候補を絞り込む（適用可能性判定）
// 3) 複数候補が残る場合は “より具体的（more specific）” なものを選ぶ（オーバーロード解決）
// 4) 解決不能（曖昧）ならコンパイルエラー
//
// この例では候補が明確なので、greet() は引数なし版、greet("Usagi") は String 引数版が選ばれる。

public class Main {
  public static void main(String args[]) {
    // ------------------------------------------------------------
    // 1) greet() 呼び出し：引数なしなので greet() が選ばれる
    // ------------------------------------------------------------
    greet();
    /*
      呼び出し式：greet()
      - 引数個数 = 0
      - 候補：
          greet()            ← 適用可能（引数0）
          greet(String name) ← 不適用（引数1必要）
      => greet() が一意に選ばれる
    */

    // ------------------------------------------------------------
    // 2) greet("Usagi") 呼び出し：String 1個なので greet(String) が選ばれる
    // ------------------------------------------------------------
    greet("Usagi");
    /*
      呼び出し式：greet("Usagi")
      - "Usagi" は文字列リテラルで型は String
      - 引数個数 = 1
      - 候補：
          greet()            ← 不適用（引数0）
          greet(String name) ← 適用可能（String 1個）
      => greet(String) が一意に選ばれる
    */
  }

  // ------------------------------------------------------------
  // 3) greet()：引数なし版（デフォルト挨拶）
  // ------------------------------------------------------------
  static void greet() {
    // ✅ System.out.println は標準出力に改行付きで文字列を出す
    System.out.println("Hello!");
  }
  /*
    ✅ シグネチャ（型的な識別子）：
      greet()

    Java のオーバーロードでは “戻り値の型だけ違う” は許されない。
    どの greet を呼ぶかは引数リストで決まるため、戻り値だけ変えても区別不能になるからである。
  */

  // ------------------------------------------------------------
  // 4) greet(String name)：名前付き挨拶版
  // ------------------------------------------------------------
  static void greet(String name) {
    // ✅ 文字列連結 "+" によって "Hi, " + name + "!" を作る
    // Java では String 連結はコンパイル時に StringBuilder 等へ最適化されることが多い
    System.out.println("Hi, " + name + "!");
  }
  /*
    ✅ シグネチャ：
      greet(String)

    引数 name はローカル変数としてこのメソッド内で使える。
    greet() と greet(String) は “同名” だが、引数リストが違うため共存できる。
  */
}