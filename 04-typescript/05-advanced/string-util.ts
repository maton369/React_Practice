// ✅ このコードは TypeScript の組み込み “文字列操作ユーティリティ型”
// - Lowercase<T>：小文字化
// - Uppercase<T>：大文字化
// - Uncapitalize<T>：先頭1文字だけ小文字化
// - Capitalize<T>：先頭1文字だけ大文字化
// を、文字列リテラル union 型に対して適用する例である。
//
// アルゴリズム的には：
// - union 型（有限集合）を集合として見て、各要素（各文字列）に同じ変換を適用する
// - 変換は要素ごとに行われ、結果は再び union として合成される
// （conditional type に近い “分配的な” 動きのイメージ）

// ------------------------------------------------------------
// 1) Company：企業名の有限集合（文字列リテラル union）
// ------------------------------------------------------------
type Company = 'Apple' | 'IBM' | 'GitHub';
/*
  ✅ Company は集合で言えば { "Apple", "IBM", "GitHub" }。
  ここに対して “表記変換” を型レベルで一括適用する。
*/

// ------------------------------------------------------------
// 2) C1：Lowercase で全て小文字化
// ------------------------------------------------------------
type C1 = Lowercase<Company>;
/*
  変換結果（概念）：
    "Apple"  -> "apple"
    "IBM"    -> "ibm"
    "GitHub" -> "github"

  => C1 は "apple" | "ibm" | "github"

  ✅ 用途例：
  - ID やURL、キー名を小文字統一したい
  - 大文字小文字を吸収した正規化表現を型で持ちたい
*/

// ------------------------------------------------------------
// 3) C2：Uppercase で全て大文字化
// ------------------------------------------------------------
type C2 = Uppercase<Company>;
/*
  変換結果（概念）：
    "Apple"  -> "APPLE"
    "IBM"    -> "IBM"
    "GitHub" -> "GITHUB"

  => C2 は "APPLE" | "IBM" | "GITHUB"

  ✅ “IBM” は既に大文字なので変換しても同じ値になる点が分かりやすい。
*/

// ------------------------------------------------------------
// 4) C3：Uncapitalize で先頭1文字だけ小文字化
// ------------------------------------------------------------
type C3 = Uncapitalize<Company>;
/*
  変換結果（概念）：
    "Apple"  -> "apple"   （先頭 A が a に）
    "IBM"    -> "iBM"     （先頭 I が i に、残りはそのまま）
    "GitHub" -> "gitHub"  （先頭 G が g に、残りはそのまま）

  => C3 は "apple" | "iBM" | "gitHub"

  ✅ Lowercase と違い “全文” を変えるのではなく、先頭1文字だけ変える。
  そのため "IBM" が "ibm" ではなく "iBM" になるのがポイント。
*/

// ------------------------------------------------------------
// 5) C4：Capitalize で C3 の先頭1文字だけ大文字化
// ------------------------------------------------------------
type C4 = Capitalize<C3>;
/*
  変換結果（概念）：
    "apple"  -> "Apple"
    "iBM"    -> "IBM"
    "gitHub" -> "GitHub"

  => C4 は "Apple" | "IBM" | "GitHub"
  つまり元の Company に戻る。

  ✅ アルゴリズム的には：
  - Uncapitalize で先頭だけ落とし、
  - Capitalize で先頭だけ上げる
  という可逆変換（このケースでは元の集合に復元）になっている。
*/

// ✅ まとめ：
// - Lowercase / Uppercase：全文の正規化
// - Uncapitalize / Capitalize：先頭1文字のケース変換
// - union に適用すると、各要素に変換がかかって結果も union になる