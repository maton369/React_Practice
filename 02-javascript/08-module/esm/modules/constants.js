/*
  ✅ このコードは ES Modules（ESM）の「名前付きエクスポート（named export）」の例である。
  モジュール外部に対して、定数 ONE / TWO / TEN を公開する。

  CommonJS（module.exports / exports）と違い、ESM の export は
  - “モジュールの公開インターフェース” を静的に宣言する（構文として解析できる）
  - import 側は名前で参照する
  という特徴がある。

  ------------------------------------------------------------
  1) export のアルゴリズム（概念）：モジュールの“公開バインディング”を作る
  ------------------------------------------------------------
  ES Modules では、モジュールが読み込まれるときに

  (1) モジュールのトップレベルを解析して、export 一覧（公開名）を確定する
      - どの名前を外に出すかが “静的に” 分かる（実行前に分かる）

  (2) import 側は、その公開名（ONE, TWO, TEN）に対する参照を持つ
      - 値のコピーではなく “バインディング（束縛）” を参照するモデル（live binding）

  (3) モジュール本体が評価され、定数が初期化される

  この “公開名を先に確定する” というのが ESM の重要な性質で、
  ツリーシェイキング（未使用exportの削除）などが可能になる。

  ------------------------------------------------------------
  2) `export { ONE, TWO }` の意味
  ------------------------------------------------------------
  これは「既に存在するローカル変数 ONE / TWO を、同名で外部へ公開する」という宣言。

  - ローカル名：ONE
  - 公開名：ONE

  - ローカル名：TWO
  - 公開名：TWO

  という対応付けになる。

  import 側では次のように受け取れる：

    import { ONE, TWO } from './constants.js';

  ------------------------------------------------------------
  3) `export const TEN = 10;` の意味
  ------------------------------------------------------------
  これは「宣言と同時に export する」書き方で、
  モジュール内に TEN を定義しつつ、そのまま公開する。

  import 側：

    import { TEN } from './constants.js';

  ------------------------------------------------------------
  4) 2つの書き方の違い（設計の観点）
  ------------------------------------------------------------
  - `const ONE = 1; ... export { ONE };`
      -> 値や名前を先に作って、まとめて export できる（一覧性が出る）
  - `export const TEN = 10;`
      -> 宣言と公開が一体で短い（単発の定数に向く）

  どちらも最終的な公開インターフェースとしては同じ「named export」である。

  ------------------------------------------------------------
  5) 補足：export した値は基本 “読み取り専用” として扱われる
  ------------------------------------------------------------
  const なのでモジュール内でも再代入できないし、
  import 側でも import した識別子へ再代入はできない（構文エラー）。
  （ESMは “参照（binding）” を扱うため、import 側は書き換え不可）

  ------------------------------------------------------------
  6) このモジュールが提供する公開API
  ------------------------------------------------------------
  - ONE : 1
  - TWO : 2
  - TEN : 10
*/

const ONE = 1;
const TWO = 2;

// ✅ 既に宣言済みの定数を、同名の named export として公開する
export { ONE, TWO };

// ✅ 宣言と同時に named export として公開する（TEN も外部から import 可能になる）
export const TEN = 10;