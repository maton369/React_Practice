/*
  ✅ このコードは ES Modules（ESM）の「名前付きエクスポート（named export）」で、
  ドイツ語っぽい定数（EINS=1, ZWEI=2, ...）をまとめて公開している。

  典型的な用途は：
  - 他モジュールから import して定数として使う（マジックナンバー回避）
  - barrel module 側で `export * as German from './constants2.js'` のように
    名前空間にまとめて公開する（衝突回避・グルーピング）

  ------------------------------------------------------------
  1) ESM の export のアルゴリズム（概念）
  ------------------------------------------------------------
  ES Modules は “公開インターフェースを静的に宣言” する。
  つまり、実行前に構文解析段階で
  - どの名前が export されるか（EINS, ZWEI, ...）
  が確定する。

  その後、モジュールが評価されると
  - const 宣言が初期化され
  - それらのバインディングが export 表面に接続される（live binding）

  ※ 今回は const なので値は変化しないが、仕組みとしては “参照（binding）を公開する”。

  ------------------------------------------------------------
  2) export { ... } の意味：既にある変数をまとめて公開
  ------------------------------------------------------------
  `export { EINS, ZWEI, DREI, VIER, FUNF };` は
  “ローカルに存在する識別子を、そのまま同名で公開する” 宣言である。

  import 側は次のように使える：

    import { EINS, FUNF } from './constants2.js';

  また、barrel 側で

    export * as German from './constants2.js';

  のように再公開されていれば、

    import { German } from './index.js';
    German.EINS

  のように名前空間経由でアクセスできる。

  ------------------------------------------------------------
  3) このモジュールが提供する公開API
  ------------------------------------------------------------
  - EINS  : 1
  - ZWEI  : 2
  - DREI  : 3
  - VIER  : 4
  - FUNF  : 5
*/

const EINS = 1;  // ✅ “1” を意味する定数
const ZWEI = 2;  // ✅ “2” を意味する定数
const DREI = 3;  // ✅ “3” を意味する定数
const VIER = 4;  // ✅ “4” を意味する定数
const FUNF = 5;  // ✅ “5” を意味する定数

// ✅ 名前付きエクスポート：上で定義した定数を同名でまとめて公開する
export { EINS, ZWEI, DREI, VIER, FUNF };