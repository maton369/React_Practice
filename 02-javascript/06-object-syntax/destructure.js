/*
  ✅ このコードは「分割代入（destructuring assignment）」の基本例である。
  分割代入は
  - 配列（イテラブル）から「位置」で取り出す
  - オブジェクトから「キー名」で取り出す
  という2系統がある。

  Reactでも props の取り出し（const { title, onClick } = props）や
  useState の返り値（const [state, setState] = useState(...)）で頻出なので、
  「束縛（binding）のアルゴリズム」として理解しておくと強い。

  ------------------------------------------------------------
  0) アルゴリズムの大枠：分割代入は “束縛のルール”
  ------------------------------------------------------------
  分割代入は「右辺の値」から「左辺に書いたパターン」に従って値を取り出し、
  それぞれの変数を初期化する構文である。

  - 配列パターン `[...]` は “順番（インデックス）” に沿って対応付ける
  - オブジェクトパターン `{...}` は “プロパティ名” に沿って対応付ける

  ------------------------------------------------------------
  1) 配列の分割代入：先頭から順に対応付ける
  ------------------------------------------------------------
*/

const [a, b] = ['foo', 'bar'];
/*
  ✅ 配列パターンの束縛
  - a は右辺配列の 0番目（'foo'）
  - b は右辺配列の 1番目（'bar'）
  という “位置対応” で代入される。
*/
console.log(a, b); // foo bar

/*
  ------------------------------------------------------------
  2) 配列の分割代入：カンマでスキップ（無視）
  ------------------------------------------------------------
*/

const [, n] = [1, 4];
/*
  ✅ 先頭要素をスキップ
  - 左辺の最初の要素が空（`,` の前に変数がない）なので 0番目は捨てる
  - n は 1番目（4）を受け取る

  つまり、[1,4] の “2番目だけ欲しい” という意図を表している。
*/
console.log(n); // 4

/*
  ------------------------------------------------------------
  3) 配列の分割代入：複数スキップ（インデックスを飛ばす）
  ------------------------------------------------------------
*/

const [, , i, , j, , , k] = [1, 2, 3, 4, 5, 6, 7];
/*
  ✅ 位置で取り出すが、間に空要素を挟んで飛ばせる

  左辺パターンをインデックス対応で見ると：

  - 0番目：スキップ
  - 1番目：スキップ
  - 2番目：i に束縛  → i = 3
  - 3番目：スキップ
  - 4番目：j に束縛  → j = 5
  - 5番目：スキップ
  - 6番目：スキップ or k？
    ここで注意点がある。

  ⚠️ 右辺は [1,2,3,4,5,6,7] で “要素は0〜6番目まで” しかない。
  左辺は k を 7番目（インデックス7）に置こうとしている形になっているため、
  k に対応する値が存在せず、k は undefined になる。

  まとめ：
    i = 3
    j = 5
    k = undefined

  ※ もし k=7 を取りたいなら、左辺は `[, , i, , j, , k]` のように
     “スキップ数” を調整する必要がある。
*/
console.log(i, j, k); // 3 5 undefined

/*
  ------------------------------------------------------------
  4) オブジェクトの分割代入：キー名で対応付ける
  ------------------------------------------------------------
*/

const profile = { name: 'Kanae', age: 24, gender: 'f' };
const { name, age } = profile;
/*
  ✅ オブジェクトパターンの束縛
  - name は profile.name を取り出す
  - age  は profile.age  を取り出す

  配列と違って “位置” ではなく “プロパティ名（キー）” で対応する。

  そのため profile のキー順が変わっても同じ結果になる。
*/
console.log(name, age); // Kanae 24

/*
  ------------------------------------------------------------
  5) よくある実務・Reactでの使い方（イメージ）
  ------------------------------------------------------------
  - useState の返り値（配列）：
      const [count, setCount] = useState(0);

  - props の取り出し（オブジェクト）：
      const MyComp = ({ title, onClick }) => ...

  分割代入は「欲しいものだけ取り出し、残りは無視する」ための束縛ルールである。
*/