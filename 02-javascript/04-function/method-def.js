/*
  ✅ このコードは「オブジェクトのプロパティ」と「メソッド呼び出し（thisの文脈）」の基本例である。
  ReactというよりJavaScriptのオブジェクト/関数の話だが、
  Reactでもイベントハンドラやコールバックで this/参照の扱いを誤るとバグになりやすいので重要。

  ------------------------------------------------------------
  1) 何が起きる？（結論）
  ------------------------------------------------------------
  - foo はオブジェクト（連想配列のようにキー→値を持つ）である。
  - foo.baz は “関数” が入ったプロパティである。
  - foo.baz() のように「プロパティ参照 + ()」で呼ぶと、それは “メソッド呼び出し” になる。
  - 実行結果として console に
      I am `baz` method
    が出力される。

  ------------------------------------------------------------
  2) オブジェクトの構造（データと振る舞いの同居）
  ------------------------------------------------------------
  const foo = {
    bar: "bar",
    baz: function () { ... },
  };

  これは以下の2種類のプロパティを持つ：

  - bar: 文字列 "bar"（データ）
  - baz: 関数（振る舞い）
    → オブジェクトに格納された関数を「メソッド」と呼ぶことが多い

  重要：
  「メソッド」というのは特別な構文ではなく、
  “オブジェクトのプロパティとして関数が入っているだけ” である。

  ------------------------------------------------------------
  3) 呼び出しアルゴリズム：`foo.baz()` は何をしている？
  ------------------------------------------------------------
  `foo.baz()` は概念的に次の2ステップで動く：

  (1) プロパティ参照
      - foo の中から "baz" というキーの値を取り出す
      - 取り出した値は関数オブジェクト

  (2) 関数呼び出し
      - その関数を () で実行する

  このとき “呼び出し形” が重要で、
  `obj.method()` 形式は「メソッド呼び出し（method call）」として扱われ、
  関数内の this は（非 strict 例外などを除けば） obj を指す。

  今回の baz は this を使っていないので目に見える差はないが、
  this を使うメソッドでは挙動が変わる。

  例：
    const foo = {
      x: 1,
      baz: function() { console.log(this.x); }
    };
    foo.baz(); // 1（this は foo）

  ------------------------------------------------------------
  4) 注意：関数を取り出して呼ぶと this が変わる
  ------------------------------------------------------------
  次のように「メソッドを変数に入れてから呼ぶ」と、呼び出し形が変わる：

    const f = foo.baz;
    f(); // this は foo ではなくなる（strict mode なら undefined）

  つまり “this は関数が定義された場所ではなく、呼び出し方で決まる” のがJSの仕様である。

  Reactでは class コンポーネント時代に this バインド問題が頻発したが、
  関数コンポーネント + Hooks が主流になったことでこの手の事故は減った。
  ただし、オブジェクトにメソッドを持たせて使う設計では依然として注意が必要。

  ------------------------------------------------------------
  5) 省略記法（参考）
  ------------------------------------------------------------
  オブジェクトリテラルでは、メソッドは次の短縮形でも書ける：

    const foo = {
      bar: "bar",
      baz() {
        console.log("I am `baz` method");
      }
    };

  意味はほぼ同じで、読みやすさのためにこちらが好まれることも多い。
*/

const foo = {
  bar: "bar", // ✅ 文字列プロパティ（データ）
  baz: function () {
    // ✅ 関数プロパティ（= メソッド）
    console.log("I am `baz` method"); // 呼ばれたらこのログを出す
  },
};

// ✅ メソッド呼び出し：foo の baz プロパティ（関数）を this=foo の文脈で実行する
foo.baz(); // -> "I am `baz` method"