/*
  ✅ このコードは「残余引数（rest parameters）...args」を使って、
    可変長（何個でも）引数を “配列として” 受け取る例である。

  ------------------------------------------------------------
  1) 結果（何が出力される？）
  ------------------------------------------------------------
  呼び出しは：
    showAllArgs("Moon", "Mercury", "Mars", "Jupiter", "Venus");

  なので args には渡した引数が順番通りすべて配列で入る。

    args = ["Moon", "Mercury", "Mars", "Jupiter", "Venus"]

  そのため console には概ね次が出る：
    ["Moon", "Mercury", "Mars", "Jupiter", "Venus"]

  ※ 表示フォーマットは環境で多少異なるが、配列の中身は同じ。

  ------------------------------------------------------------
  2) アルゴリズム観点：引数束縛（parameter binding）の手順
  ------------------------------------------------------------
  関数呼び出し時、エンジンは「実引数（渡された値）」を
  「仮引数（関数定義側の変数）」に束縛する。

  `function showAllArgs(...args) { ... }` の場合、仮引数は残余引数のみであるため、

  - 渡された引数を左から順にすべて収集し、
  - 新しい配列を作って args に束縛する

  という動作になる。

  重要ポイント：
  - args は常に配列になる（引数0個なら []）
  - ...args は引数リストの最後にしか書けない
    （“残りを全部取る” という定義上、最後でないと曖昧になる）

  ------------------------------------------------------------
  3) rest と arguments の違い（この例の意義）
  ------------------------------------------------------------
  古いJSでは可変長引数を `arguments`（配列っぽいオブジェクト）で扱うことが多かった。
  しかし rest の利点は：

  - “本物の配列” なので map/filter/reduce などがそのまま使える
  - 受け取る引数の範囲が明示的（関数シグネチャに現れる）
  - strict mode / 型付け（TypeScript）とも相性が良い

  ------------------------------------------------------------
  4) 応用例（よくある使い方）
  ------------------------------------------------------------
  - ログ関数：log(...args)
  - 合計計算：sum(...nums)
  - 配列に追加：arr.push(...items)

  Reactでも props の受け渡しで `{...rest}` をよく使うが、
  それと同じ “残りをまとめる” 発想である。
*/

function showAllArgs(...args) {
  // ✅ args は「渡された引数が全て入った配列」
  console.log(args);
}

showAllArgs("Moon", "Mercury", "Mars", "Jupiter", "Venus");