/*
  ✅ このコードは「オブジェクトリテラルのメソッド短縮記法」と
    「メソッド呼び出し（member call）による this 文脈の付与」を示す例である。

  Reactそのものではないが、JSの “関数は値” という性質と
  “呼び出し方で this が決まる” という仕様は、コールバック設計で重要になる。

  ------------------------------------------------------------
  1) 何が起きる？（結論）
  ------------------------------------------------------------
  - fuu は bar（文字列）と baz（関数）を持つオブジェクトである。
  - `fuu.baz()` を実行するとコンソールに
      I am `baz` method
    が表示される。

  ------------------------------------------------------------
  2) `baz() { ... }` は何の省略か（メソッド短縮記法）
  ------------------------------------------------------------
  次の2つは基本的に同じ意味になる：

  A) 短縮記法（今回）
      baz() { ... }

  B) 従来の書き方
      baz: function () { ... }

  どちらも「baz というプロパティに関数オブジェクトを格納する」点は同じ。
  つまり “メソッド” は特別な別概念ではなく、
  「オブジェクトに入った関数プロパティ」を慣習的にそう呼んでいるだけである。

  ------------------------------------------------------------
  3) 呼び出しアルゴリズム：`fuu.baz()` の評価ステップ
  ------------------------------------------------------------
  `fuu.baz()` は概念的に次の処理をしている：

  (1) プロパティ参照（Member Access）
      - fuu から "baz" の値（関数）を取り出す

  (2) 呼び出し（Call）
      - 取り出した関数を () で実行する

  重要：
  `obj.prop()` の形で呼ぶと “メソッド呼び出し（member call）” として扱われ、
  関数内部の this が obj（ここでは fuu）になる。

  今回は this を使っていないので結果には表れないが、
  this を参照するメソッドなら挙動差が出る。

  ------------------------------------------------------------
  4) 注意点：取り出して呼ぶと this が失われる
  ------------------------------------------------------------
  メソッド呼び出しは「呼び出し形」に依存するため、
  次のように参照を取り出すと挙動が変わる：

    const g = fuu.baz;
    g(); // strict mode では this は undefined になりやすい

  つまり “this は定義場所ではなく、呼び出し方で決まる”。
  Reactでは関数コンポーネント中心になって this 事故は減ったが、
  オブジェクト指向的にメソッドを渡す設計では依然として注意が必要。

  ------------------------------------------------------------
  5) 補足：bar はただのデータ、baz は振る舞い
  ------------------------------------------------------------
  - bar: "bar" は状態（データ）
  - baz() { ... } は振る舞い（手続き）
  という「データと振る舞いを同じオブジェクトに持たせる」形になっている。
*/

const fuu = {
  // ✅ データプロパティ（単なる文字列）
  bar: "bar",

  // ✅ メソッド短縮記法：baz: function() { ... } の省略形
  baz() {
    // ✅ this を使わない単純な副作用（コンソール出力）
    console.log("I am `baz` method");
  },
};

// ✅ メソッド呼び出し：member call により（必要なら）this=fuu の文脈で実行される
fuu.baz(); // -> "I am `baz` method"