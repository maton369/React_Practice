/*
  ✅ このコードは「残余引数（rest parameters）」の挙動を示す例である。
  - 最初の2つの引数を a, b に束縛し、
  - それ以降に渡された引数を “配列として” rest にまとめる。

  Reactでも props の分割（...rest）や関数引数の受け取りなどで頻出なので、
  「引数束縛アルゴリズム」として理解しておくと便利である。

  ------------------------------------------------------------
  1) 結果（何が出力される？）
  ------------------------------------------------------------
  呼び出しは：
    showNames('Moon', 'Mercury', 'Mars', 'Jupiter', 'Venus');

  なので束縛は次のようになる：

  - a = 'Moon'        （第1引数）
  - b = 'Mercury'     （第2引数）
  - rest = ['Mars', 'Jupiter', 'Venus']（第3引数以降の“残り全部”を配列化）

  よって出力は概ね次の順：

    Moon
    Mercury
    ['Mars', 'Jupiter', 'Venus']

  ※ console の表示形式は環境により多少異なることがあるが、内容は同じ。

  ------------------------------------------------------------
  2) アルゴリズム観点：引数束縛（parameter binding）
  ------------------------------------------------------------
  関数呼び出し時、エンジンは実引数（渡された値）を仮引数へ次の規則で束縛する。

  (1) 左から順に通常引数へ割り当て
      - a に第1引数
      - b に第2引数

  (2) 残余引数 ...rest には
      - “まだ割り当てられていない残り” を
      - 新しい配列としてまとめて渡す

  重要ポイント：
  - rest は常に配列になる（0個でも []）
  - rest は引数リストの最後にしか書けない
    （残りを全部取るため、途中に置くと束縛規則が定義できない）

  ------------------------------------------------------------
  3) rest と arguments の違い（参考）
  ------------------------------------------------------------
  - rest（...rest）は “本物の配列” で、map/filter など配列メソッドがそのまま使える
  - arguments は（通常関数では）配列っぽいが配列ではない “array-like”
    （アロー関数は arguments を持たないので、可変長引数は基本 rest を使う）

  ------------------------------------------------------------
  4) よくある利用パターン（Reactでもよく出る）
  ------------------------------------------------------------
  - 引数や props の「先頭だけ取り出して、残りはまとめて扱う」
    例：const Component = ({ title, ...restProps }) => <div {...restProps} />
  - 可変長引数の関数（ログ関数、ユーティリティなど）
*/

const showNames = (a, b, ...rest) => {
  // ✅ a は第1引数が入る
  console.log(a);

  // ✅ b は第2引数が入る
  console.log(b);

  // ✅ rest は第3引数以降の“残り全部”が配列で入る
  //    この呼び出しだと ['Mars', 'Jupiter', 'Venus'] になる
  console.log(rest);
};

showNames('Moon', 'Mercury', 'Mars', 'Jupiter', 'Venus');