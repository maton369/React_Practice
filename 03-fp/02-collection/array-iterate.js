// ✅ このコードは、配列 `arr` に対して
// - map / filter / find / findIndex / every / some
// という典型的な配列メソッドを一気に実行し、それぞれの結果を console.log で並べて表示する例である。
//
// 重要ポイント：
// - これらは「配列を左から順に走査する」線形アルゴリズム（基本 O(N)）である
// - ただし find/findIndex/every/some は “条件が満たされた時点で短絡終了” するため、平均的には早く終わり得る
// - map/filter は基本的に最後まで走査し、新しい配列を作って返す（非破壊）
//
// ------------------------------------------------------------
// 0) 入力データ
// ------------------------------------------------------------
const arr = [1, 2, 3, 4, 5, 6, 7, 8, 9];

// ------------------------------------------------------------
// 1) arr.map((n) => n * 2)
// ------------------------------------------------------------
// ✅ map のアルゴリズム（写像 / 変換）
// - 配列を左から順に 1 要素ずつ走査する
// - コールバック関数（ここでは n => n * 2）を各要素に適用する
// - 返り値を同じインデックスに配置して “同じ長さ” の新しい配列を作る
//
// 入力 : [1,2,3,4,5,6,7,8,9]
// 出力 : [2,4,6,8,10,12,14,16,18]
//
// ※ 元の arr は変更されない（非破壊）。

// ------------------------------------------------------------
// 2) arr.filter((n) => n % 3 === 0)
// ------------------------------------------------------------
// ✅ filter のアルゴリズム（選別 / 抽出）
// - 配列を左から順に走査する
// - 述語（predicate）関数が true を返した要素だけを結果配列に追加する
// - 結果配列の長さは入力より短くなり得る
//
// ここでは「3で割り切れるか」を判定している：n % 3 === 0
// 入力 : [1,2,3,4,5,6,7,8,9]
// 出力 : [3,6,9]

// ------------------------------------------------------------
// 3) arr.find((n) => n > 4)
// ------------------------------------------------------------
// ✅ find のアルゴリズム（最初の一致の探索）
// - 配列を左から順に走査する
// - 条件を満たした最初の要素を見つけた時点で、その要素を返して終了（短絡）
// - 見つからなければ undefined を返す
//
// ここでは「n > 4」を満たす最初の要素 → 5
// 走査イメージ：1,2,3,4 は不一致 → 5 で一致 → 即終了
// 出力 : 5

// ------------------------------------------------------------
// 4) arr.findIndex((n) => n > 4)
// ------------------------------------------------------------
// ✅ findIndex のアルゴリズム（最初の一致位置の探索）
// - find と同様に左から順に走査し、最初に条件を満たした要素の “インデックス” を返して終了（短絡）
// - 見つからなければ -1 を返す
//
// 「n > 4」を満たす最初の要素は 5 で、arr のインデックスは 4（0始まり）
// 出力 : 4

// ------------------------------------------------------------
// 5) arr.every((n) => n !== 0)
// ------------------------------------------------------------
// ✅ every のアルゴリズム（全称判定 / “全部が条件を満たすか”）
// - 配列を左から順に走査する
// - 1つでも条件を満たさない要素があれば false を返して終了（短絡）
// - 最後まで全て満たせば true を返す
//
// ここでは「全要素が 0 ではないか」を判定している。
// arr は 1..9 なので 0 は含まれず、全て true → 最後まで走査して true
// 出力 : true

// ------------------------------------------------------------
// 6) arr.some((n) => n >= 10)
// ------------------------------------------------------------
// ✅ some のアルゴリズム（存在判定 / “どれか1つでも条件を満たすか”）
// - 配列を左から順に走査する
// - 1つでも条件を満たす要素があれば true を返して終了（短絡）
// - 最後まで見つからなければ false を返す
//
// ここでは「10以上の要素が存在するか」を判定している。
// arr は最大 9 なので該当なし → 最後まで走査して false
// 出力 : false

// ------------------------------------------------------------
// 7) console.log の出力（この入力の場合）
// ------------------------------------------------------------
// 1つの console.log に複数引数を渡しているため、結果が並んで表示される。
// - map の結果        : [2,4,6,8,10,12,14,16,18]
// - filter の結果     : [3,6,9]
// - find の結果       : 5
// - findIndex の結果  : 4
// - every の結果      : true
// - some の結果       : false
console.log(
  arr.map((n) => n * 2),
  arr.filter((n) => n % 3 === 0),
  arr.find((n) => n > 4),
  arr.findIndex((n) => n > 4),
  arr.every((n) => n !== 0),
  arr.some((n) => n >= 10),
);