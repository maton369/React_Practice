// ✅ このコードは配列 `arr` に対して `includes` を使い、
// 指定した値が配列内に存在するか（真偽値）を確認する例である。
//
// `Array.prototype.includes(value)` の本質は
// 「左から順に要素を走査して一致する値を探す線形探索アルゴリズム」。
// 見つかった時点で true を返して短絡終了し、最後まで見つからなければ false を返す。
// したがって計算量は最悪 O(N)（平均は見つかる位置次第）。

const arr = [1, 2, 3, 4, 5];

// ------------------------------------------------------------
// 1) arr.includes(5)
// ------------------------------------------------------------
// ✅ 探索アルゴリズム（概念）
// - arr[0] と 5 を比較 → 1 !== 5
// - arr[1] と 5 を比較 → 2 !== 5
// - arr[2] と 5 を比較 → 3 !== 5
// - arr[3] と 5 を比較 → 4 !== 5
// - arr[4] と 5 を比較 → 5 === 5 → 見つかったので true を返して終了（短絡）
//
// よって true が出力される。
console.log(arr.includes(5));

// ------------------------------------------------------------
// 2) arr.includes(9)
// ------------------------------------------------------------
// ✅ 探索アルゴリズム（概念）
// - 1,2,3,4,5 を順に比較するが一致しない
// - 最後まで走査しても見つからないため false を返す
//
// よって false が出力される。
console.log(arr.includes(9));

